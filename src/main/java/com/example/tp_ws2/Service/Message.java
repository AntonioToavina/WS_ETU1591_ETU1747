package com.example.tp_ws2.Service;

public class Message {
    int code=404;
    String message;

    public Message(String message){
        this.message=message;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

}
